//Method Overloading:

package com.kprit.tns.backendtraining.oops;

public class polyDemo {
	public int add(int a, int b) {
		int result = a+b;
		return result;
	}
	
	public float add(float a,float b) {
		float result =a+b;
		return result;
		
	}
	
	public double add(double a ,double b , double c) {
		double result=a+b+c;
		return result;
		
	}

}
